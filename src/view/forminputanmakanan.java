void isitabel(){
Object data[][] = new Object[record.size()][3];
int x=0;
for(Modelmakanan inimakanan : record){
data[x][0]=inimakanan.getKode_makanan();
data[x][1]=inimakanan.getNama_makanan();
data[x][2]=inimakanan.getHarga_makanan();
x++;
}
String judul[]={"Kode Makanan","Nama Makanan","Harga Makanan"};
jTable1.setModel(new DefaultTableModel(data,judul));
}
void refresdata(){
panggildata();
isitabel();

}


